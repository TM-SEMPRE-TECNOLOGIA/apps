// Map icon string to component
export function ToolIcon({ icon, size = 18 }: { icon: string; size?: number }) {
  const props = { size }
  switch (icon) {
    case 'edit': return <IconEdit {...props} />
    case 'compare': return <IconCompare {...props} />
    case 'mail': return <IconMail {...props} />
    case 'bot': return <IconBot {...props} />
    case 'settings': return <IconSettings {...props} />
    case 'file': return <IconFileText {...props} />
    case 'camera': return <IconCamera {...props} />
    case 'folder': return <IconFolder {...props} />
    case 'bar-chart': return <IconBarChart {...props} />
    default: return <IconGrid {...props} />
  }
}

